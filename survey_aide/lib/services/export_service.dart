Future<Uint8List> generateQuoteImage(List<QuoteEntry> items) async {
  const double w = 600;
  const double headerH = 80;
  const double footerH = 40;
  const double lineH = 24;
  const double margin = 20;

  final grouped = <String, List<QuoteEntry>>{};
  for (final item in items) {
    grouped.putIfAbsent(item.client, () => []).add(item);
  }

  final rows = <String, String>{};
  double grandTotal = 0;
  for (final entry in grouped.entries) {
    rows['client_${entry.key}'] = 'Client: ${entry.key}';
    if (entry.value.first.location.isNotEmpty) {
      rows['loc_${entry.key}'] = 'Location: ${entry.value.first.location}';
    }
    rows['sp1_${entry.key}'] = '';
    double clientTotal = 0;
    for (final item in entry.value) {
      rows['svc_${item.uid}'] = serviceLabel(item.code, item.name);
      for (final l in item.lines) {
        rows['line_${item.uid}_${l.label}'] = '  ${l.label}: ${peso(l.amount)}';
      }
      final st = item.lines.fold<double>(0, (s, l) => s + l.amount);
      rows['sub_${item.uid}'] = '  Subtotal: ${peso(st)}';
      rows['sp2_${item.uid}'] = '';
      clientTotal += st;
    }
    rows['ctot_${entry.key}'] = 'Client Total: ${peso(clientTotal)}';
    rows['sp3_${entry.key}'] = '';
    grandTotal += clientTotal;
  }
  rows['grand'] = 'GRAND TOTAL: ${peso(grandTotal)}';

  final lineKeys = rows.keys.toList();
  final totalHeight = headerH + (lineKeys.length * lineH) + footerH + margin * 2;

  final recorder = ui.PictureRecorder();
  final canvas = Canvas(recorder, Rect.fromLTWH(0, 0, w, totalHeight));

  canvas.drawRect(Rect.fromLTWH(0, 0, w, totalHeight),
      Paint()..color = const Color(0xFFFBF9F2));
  canvas.drawRect(const Rect.fromLTWH(0, 0, w, headerH),
      Paint()..color = const Color(0xFFE8A06B));

  void drawText(String txt, double x, double y, {
    double size = 14, Color color = const Color(0xFF1A1E1D), ui.FontWeight? weight, bool center = false,
  }) {
    final pb = ui.ParagraphBuilder(
      ui.ParagraphStyle(
        textAlign: center ? ui.TextAlign.center : ui.TextAlign.left,
        fontSize: size,
        fontWeight: weight,
      ),
    )
      ..pushStyle(ui.TextStyle(color: color, fontSize: size, fontWeight: weight))
      ..addText(txt);
    final constraints = ui.ParagraphConstraints(width: center ? w : w - 2 * margin);
    final layout = pb.build()..layout(constraints);
    canvas.drawParagraph(layout, Offset(x, y));
  }

  drawText('Survey Aide', 0, 24, size: 24, color: const Color(0xFFFFFFFF), weight: FontWeight.bold, center: true);

  double y = headerH + margin;
  for (final key in lineKeys) {
    final txt = rows[key]!;
    final isGrand = txt.startsWith('GRAND');
    final isClient = txt.startsWith('Client') || txt.startsWith('GRAND');
    drawText(txt, margin, y,
      size: isGrand ? 16 : 14,
      color: isGrand ? const Color(0xFFC84A1E) : const Color(0xFF1A1E1D),
      weight: isClient ? FontWeight.bold : null,
    );
    y += lineH;
  }

  canvas.drawRect(Rect.fromLTWH(0, totalHeight - footerH, w, footerH),
      Paint()..color = const Color(0xFFE0DACB));
  drawText('Generated by Survey Aide', 0, totalHeight - footerH + 12,
      size: 12, color: const Color(0xFF6D6355), center: true);

  final picture = recorder.endRecording();
  final img = await picture.toImage(w.toInt(), totalHeight.toInt());
  final byteData = await img.toByteData(format: ui.ImageByteFormat.png);
  return byteData!.buffer.asUint8List();
}

