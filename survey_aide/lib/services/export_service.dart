Future<Uint8List> generatePdfBytes(List<QuoteEntry> items) async {
  final pdf = pw.Document();
  pdf.addPage(
    pw.MultiPage(
      pageFormat: PdfPageFormat.a4,
      margin: const pw.EdgeInsets.all(32),
      header: (context) => pw.Container(
        color: const PdfColor.fromInt(0xFFE8A06B),
        padding: const pw.EdgeInsets.all(16),
        child: pw.Center(
          child: pw.Text('Survey Aide — Payments',
            style: const pw.TextStyle(color: PdfColors.white, fontSize: 20, fontWeight: pw.FontWeight.bold)),
        ),
      ),
      footer: (context) => pw.Container(
        color: const PdfColor.fromInt(0xFFE0DACB),
        padding: const pw.EdgeInsets.all(8),
        child: pw.Text('Generated by Survey Aide',
          style: const pw.TextStyle(color: PdfColor.fromInt(0xFF6D6355), fontSize: 10)),
      ),
      build: (context) {
        final grouped = <String, List<QuoteEntry>>{};
        for (final item in items) {
          grouped.putIfAbsent(item.client, () => []).add(item);
        }

        final content = <pw.Widget>[];
        double grandTotal = 0;

        for (final entry in grouped.entries) {
          content.add(pw.SizedBox(height: 16));
          content.add(pw.Text('Client: ${entry.key}',
            style: const pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold)));
          if (entry.value.first.location.isNotEmpty) {
            content.add(pw.Text('Location: ${entry.value.first.location}',
              style: const pw.TextStyle(fontSize: 12, color: PdfColor.fromInt(0xFF6D6355))));
          }
          content.add(pw.Divider());

          double clientTotal = 0;
          final rows = <pw.TableRow>[];

          for (final item in entry.value) {
            rows.add(pw.TableRow(
              children: [
                pw.Padding(padding: const pw.EdgeInsets.all(4), child: pw.Text(serviceLabel(item.code, item.name), style: const pw.TextStyle(fontWeight: pw.FontWeight.bold))),
                pw.Padding(padding: const pw.EdgeInsets.all(4), child: pw.Text('')),
              ],
            ));
            for (final line in item.lines) {
              rows.add(pw.TableRow(
                children: [
                  pw.Padding(padding: const pw.EdgeInsets.only(left: 16, top: 2, bottom: 2), child: pw.Text(line.label)),
                  pw.Padding(padding: const pw.EdgeInsets.all(2), child: pw.Text(peso(line.amount), textAlign: pw.TextAlign.right)),
                ],
              ));
            }
            final st = item.lines.fold<double>(0, (s, l) => s + l.amount);
            rows.add(pw.TableRow(
              children: [
                pw.Padding(padding: const pw.EdgeInsets.only(left: 16, top: 4, bottom: 4), child: pw.Text('Subtotal', style: const pw.TextStyle(fontWeight: pw.FontWeight.bold))),
                pw.Padding(padding: const pw.EdgeInsets.all(4), child: pw.Text(peso(st), textAlign: pw.TextAlign.right, style: const pw.TextStyle(fontWeight: pw.FontWeight.bold))),
              ],
            ));
            clientTotal += st;
          }

          content.add(pw.Table(
            border: pw.TableBorder.all(color: const PdfColor.fromInt(0xFFE0DACB)),
            children: rows,
          ));

          content.add(pw.SizedBox(height: 8));
          content.add(pw.Text('Client Total: ${peso(clientTotal)}',
            style: const pw.TextStyle(fontSize: 14, fontWeight: pw.FontWeight.bold)));
          grandTotal += clientTotal;
        }

        content.add(pw.SizedBox(height: 16));
        content.add(pw.Divider());
        content.add(pw.Text('GRAND TOTAL: ${peso(grandTotal)}',
          style: const pw.TextStyle(fontSize: 18, fontWeight: pw.FontWeight.bold, color: PdfColor.fromInt(0xFFC84A1E))));

        return content;
      },
    ),
  );

  return pdf.save();
}

Future<void> generatePdf(List<QuoteEntry> items) async {
  final bytes = await generatePdfBytes(items);
  final dir = await getTemporaryDirectory();
  final file = File('${dir.path}/quote_receipt.pdf');
  await file.writeAsBytes(bytes);
  await Printing.layoutPdf(onLayout: (_) => bytes);
}

Future<void> printPdf(List<QuoteEntry> items) async {
  final bytes = await generatePdfBytes(items);
  await Printing.layoutPdf(onLayout: (_) => bytes);
}
